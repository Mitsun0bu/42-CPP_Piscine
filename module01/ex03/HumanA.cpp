# include "HumanA.hpp"

/* ************************************************************************** */
/*                                                                            */
/*                           ~~~ CONSTRUCTORS ~~~                             */
/*                                                                            */
/* ************************************************************************** */

HumanA::HumanA(std::string name, Weapon &weapon): _name(name), _weapon(&weapon)
{
    std::cout << this->_name
              << " is ready to battle !"
              << std::endl;
    return ;
}

/* ************************************************************************** */
/*                                                                            */
/*                          ~~~ MEMBER FUNCTIONS ~~~                          */
/*                                                                            */
/* ************************************************************************** */

void    HumanA::attack(void)
{
    std::cout << this->_name
              << " attacks with his "
              << this->_weapon->getType()
              << std::endl;
    return ;
}

/* ************************************************************************** */
/*                                                                            */
/*                           ~~~ DESTRUCTOR ~~~                               */
/*                                                                            */
/* ************************************************************************** */

HumanA::~HumanA(void)
{
    std::cout << this->_name
              << " has left the battlefield !"
              << std::endl;
    return ;
}
