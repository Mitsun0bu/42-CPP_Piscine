/* ************************************************************************** */
/*                                                                            */
/*                           ~~~ CONSTRUCTORS ~~~                             */
/*                                                                            */
/* ************************************************************************** */

Zombie::Zombie(void)
{
    static int  i = 0;

    this->_i = i;
    std::cout << "Zombie #"
              << this->_i
              << " from the MegaHorde"
              << this->_name
              << " has been created !" << std::endl;
    i ++;
    return ;
}

/* ************************************************************************** */
/*                                                                            */
/*                          ~~~ MEMBER FUNCTIONS ~~~                          */
/*                                                                            */
/* ************************************************************************** */

void    Zombie::announce(void) const
{
    std::cout << "Zombie #"
              << this->_i
              << " : I want BraiiiiiiinnnzzzZ..." << std::endl;
    return ;
}

void    Zombie::set_name(std::string name)
{
    this->_name = name;
    return ;
}

/* ************************************************************************** */
/*                                                                            */
/*                           ~~~ DESTRUCTOR ~~~                               */
/*                                                                            */
/* ************************************************************************** */

Zombie::~Zombie(void)
{
    std::cout << "Zombie #"
              << this->_i
              << " has been killed !" << std::endl;
    return ;
}
