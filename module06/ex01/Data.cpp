# include "Data.hpp"

/* ************************************************************************** */
/*                                                                            */
/*                           ~~~ CONSTRUCTORS ~~~                             */
/*                                                                            */
/* ************************************************************************** */

Data::Data(void) : _value(42)
{
    std::cout << std::endl << BLUE << "[CONSTRUCTOR] : " << END
			  << "Data has been created." << std::endl
              << std::endl;
	return ;
}

Data::Data(Data& src)
{
    std::cout << ORANGE << "[COPY CONSTRUCTOR] : " << END
    << "Data has been duplicated !" << std::endl
    << std::endl;
    *this = src;
    return ;
}

/* ************************************************************************** */
/*                                                                            */
/*                          ~~~ OPERATOR OVERLOAD ~~~                         */
/*                                                                            */
/* ************************************************************************** */

Data&   Data::operator=(Data const & src)
{
    this->_value = src._value;
    return (*this);
}

/* ************************************************************************** */
/*                                                                            */
/*                          ~~~ MEMBER FUNCTIONS ~~~                          */
/*                                                                            */
/* ************************************************************************** */

int Data::getValue(void)
{
    return (this->_value);
}

/* ************************************************************************** */
/*                                                                            */
/*                           ~~~ DESTRUCTOR ~~~                               */
/*                                                                            */
/* ************************************************************************** */

Data::~Data(void)
{
    std::cout << std::endl << PURPLE << "[DESTRUCTOR] : " << END
			  << "Data has been destructed." << std::endl
              << std::endl;
	return ;
}