Animal::Animal(void) : _type("mammal")
{
	std::cout << "An animal is born !" << std::endl;
	return ;
}

Animal::Animal(Animal const &src)
{
	*this = src;
	return ;
}

/* ************************************************************************** */
/*                                                                            */
/*                          ~~~ OPERATOR OVERLOAD ~~~                         */
/*                                                                            */
/* ************************************************************************** */

Animal	&Animal::operator=(Animal const &src)
{
	this->_type = src.getType();
	return *this;
}

