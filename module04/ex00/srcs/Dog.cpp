/* ************************************************************************** */
/*                                                                            */
/*                           ~~~ CONSTRUCTORS ~~~                             */
/*                                                                            */
/* ************************************************************************** */

Dog::Dog(void)
{
	std::cout << "The newborn animal is a dog !" << std::endl;
	this->_type = "dog";
	return ;
}

Dog::Dog(Dog const &src)
{
	*this = src;
	return ;
}

/* ************************************************************************** */
/*                                                                            */
/*                          ~~~ OPERATOR OVERLOAD ~~~                         */
/*                                                                            */
/* ************************************************************************** */

Dog&	Dog::operator=(Dog const &src)
{
	this->_type = src.getType();
	return (*this);
}

/* ************************************************************************** */
/*                                                                            */
/*                          ~~~ MEMBER FUNCTIONS ~~~                          */
